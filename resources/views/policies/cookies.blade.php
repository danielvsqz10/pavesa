<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Cargar las preferencias guardadas
        const savedPreferences = JSON.parse(localStorage.getItem('cookie-preferences') || '{}');

        // Establecer los valores de los checkboxes
        document.getElementById('performance-cookies').checked = savedPreferences.performance === true;
        document.getElementById('functionality-cookies').checked = savedPreferences.functionality === true;
        document.getElementById('advertising-cookies').checked = savedPreferences.advertising === true;

        // Guardar preferencias
        document.getElementById('save-cookies-preferences').addEventListener('click', function() {
            const preferences = {
                essential: true, // Siempre activadas
                performance: document.getElementById('performance-cookies').checked,
                functionality: document.getElementById('functionality-cookies').checked,
                advertising: document.getElementById('advertising-cookies').checked
            };

            localStorage.setItem('cookie-preferences', JSON.stringify(preferences));
            localStorage.setItem('cookie-consent-accepted', 'true');

            alert('Tus preferencias han sido guardadas.');

            // Aquí podrías activar/desactivar cookies según las preferencias
            applyPreferences(preferences);
        });

        // Aceptar todas
        document.getElementById('accept-all-cookies').addEventListener('click', function() {
            const preferences = {
                essential: true,
                performance: true,
                functionality: true,
                advertising: true
            };

            document.getElementById('performance-cookies').checked = true;
            document.getElementById('functionality-cookies').checked = true;
            document.getElementById('advertising-cookies').checked = true;

            localStorage.setItem('cookie-preferences', JSON.stringify(preferences));
            localStorage.setItem('cookie-consent-accepted', 'true');

            alert('Has aceptado todas las cookies.');

            // Activar todas las cookies
            applyPreferences(preferences);
        });

        function applyPreferences(prefs) {
            // Ejemplo de código para aplicar las preferencias
            // Google Analytics
            if (prefs.performance) {
                // Activar Google Analytics
                console.log('Google Analytics activado');
            } else {
                // Desactivar Google Analytics
                console.log('Google Analytics desactivado');
                window['ga-disable-UA-XXXXXXXX-X'] = true;
            }

            // Funcionalidades adicionales
            if (prefs.functionality) {
                console.log('Cookies de funcionalidad activadas');
                // Activar widgets, etc.
            }

            // Publicidad
            if (prefs.advertising) {
                console.log('Cookies de publicidad activadas');
                // Activar cookies de publicidad
            }
        }

        // Aplicar preferencias guardadas al cargar la página
        applyPreferences(savedPreferences);
    });
</script>
